package sql_JDBC.day0313;
import java.io.*;
import javax.servlet.*;
import javax.servlet.http.*;
public class PostData {
	//1.Client Post 방식으로 Request::doPost() method O/R
	//2.service() O/R기능
	public void doGet(HttpServletRequest req, HttpServletResponse res)throws ServletException, IOException{
		//res.setCharacterEncoding("~~")(API확인)
		//16진수 인코딩된 client form data한글 인코딩
		req.setCharacterEncoding("EUC_KR");
		
		//아래의 두실행문은 servlet에서 client로 Html을 전송시 필수 코딩
		res.setContentType("text/html;charset=EUC_KR");
		PrintWriter out = res.getWriter();
		
		//client form data(QueryString::name==value)처리(API확인)
		String clientName = req.getParameter("name");
		String clientAddr = req.getParameter("addr");
		
		//client form data출력(출력은?)
		System.out.println(clientName+":"+clientAddr);
		
		out.println("<html>");
		out.println("<head><title>PostData.java</title></head>");
		out.println("<body>");
		
		out.println("<h2>Post Test</h2>");
		out.println("<li>이름:"+clientName);
		out.println("<li>주소:"+clientAddr);
		
		out.println("<p><p><a gref='/edu.postData.html'>뒤로</a>");
		
		out.println("</body>");
		out.println("</html>");
	}
}
